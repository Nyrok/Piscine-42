void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	temp;

	i = size / 2;
	while (--i >= 0)
	{
		temp = tab[size - 1 - i];
		tab[size - 1 - i] = tab[i];
		tab[i] = temp;
	}
}
